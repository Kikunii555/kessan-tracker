/**
 * 銘柄コードから銘柄名を検索
 * @param {string} code - 銘柄コード
 * @returns {string|null} 銘柄名（見つからない場合はnull）
 */
function lookupStock(code) {
  return STOCK_MASTER[code] || null;
}

/**
 * 銘柄コードを前方一致 or 銘柄名部分一致で検索（サジェスト用）
 * @param {string} query - 入力中のコードまたは銘柄名
 * @returns {Array<{code: string, name: string}>}
 */
function searchStocks(query) {
  if (!query) return [];
  const q = query.toLowerCase();
  const results = [];
  for (const [code, name] of Object.entries(STOCK_MASTER)) {
    if (code.startsWith(q) || name.toLowerCase().includes(q)) {
      results.push({ code, name });
    }
    if (results.length >= 10) break;
  }
  return results;
}
